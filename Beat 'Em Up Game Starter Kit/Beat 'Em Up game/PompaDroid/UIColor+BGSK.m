#import "UIColor+BGSK.h"

@implementation UIColor (BGSK)

+ (UIColor *)fullHPColor
{
    return [UIColor colorWithRed:95/255.0
                           green:1.0
                            blue:106/255.0
                           alpha:1.0];
}

+ (UIColor *)midHPColor
{
    return [UIColor colorWithRed:1.0
                           green:165/255.0
                            blue:0.0
                           alpha:1.0];
}

+(UIColor *)lowHPColor
{
    return [UIColor colorWithRed:1.0
                           green:50/255.0
                            blue:23/255.0
                           alpha:1.0];
}

@end
